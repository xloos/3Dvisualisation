        .catch(error => {
          console.error("There was an error fetching the course count:", error);
        });
    },
    logout() {
      axios.get('/api/users/logout') // Volanie na server na invalidáciu session/cookie
        .then(() => {
          localStorage.removeItem('isLoggedIn');
          localStorage.removeItem('username');
          window.dispatchEvent(new CustomEvent('login-change'));
